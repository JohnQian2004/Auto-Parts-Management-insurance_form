export const getCountryName = (code: string): string => {
  const country = countries.find(c => c.code === code);
  return country ? country.name : code;
};

export const getCountryCode = (name: string): string => {
  const country = countries.find(c => c.name === name);
  return country ? country.code : name;
};

export const getCountryNativeName = (code: string): string => {
  const country = countries.find(c => c.code === code);
  return country ? country.nativeName : code;
};

export const getCountryDisplayName = (code: string): string => {
  const country = countries.find(c => c.code === code);
  if (!country) return code;
  
  return country.nativeName !== country.name 
    ? `${country.name} (${country.nativeName})`
    : country.name;
}; 